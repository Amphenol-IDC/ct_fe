void print_reset_reason(void)
{
    uint32_t rsr = RCC->RSR;

    printf("\r\n===== RESET REASON: 0x%08lX =====\r\n", rsr);

    if (rsr & RCC_RSR_WWDGRSTF) printf("Window Watchdog Reset\r\n");
    if (rsr & RCC_RSR_IWDGRSTF) printf("Independent Watchdog Reset\r\n");
    if (rsr & RCC_RSR_SFTRSTF)  printf("Software Reset\r\n");
    if (rsr & RCC_RSR_BORRSTF)  printf("Power/Brown-Out Reset\r\n");
    if (rsr & RCC_RSR_PINRSTF)  printf("Pin Reset (NRST)\r\n");
    if (rsr & RCC_RSR_LPWRRSTF) printf("Low-Power Reset\r\n");

    __HAL_RCC_CLEAR_RESET_FLAGS();
}

